            <p class="Texto">
                Comparando as duas versões podemos elencar alguns pontos: 
            </p>
            <p class="Texto">
            <strong>1.</strong> a introdução da norma ficou mais sucinta e objetiva;
            </p>
            <p class="Texto">
            <strong>2.</strong> termos e definições houve uma redução, pois na versão 2009 haviam 29 itens enquanto que na versão 2018 restaram apenas 8. 
            </p>
            <p class="Texto">
            <strong>3.</strong> os princípios também houve redução de 11, na versão 2009, para 8 na versão 2018;
            </p>
            <p class="Texto">
            <strong>4.</strong> a estrutura sofreu diversas mudanças, mas sem sem muitas alterações de sentido.
            </p>

            <p class="Texto">
                Algumas ideias ganharam mais destaque do que na versão anterior, sendo:
            </p>
            <p class="Texto">
            <strong>a)</strong> Posicionamento da liderança: Assim como em outras normas ISO mais recentes, a liderança, ou Alta Direção, vem sendo posicionada como essencial para a condução das atividades de definição de responsabilidade e, especialmente, mudança de cultura por meio da demonstração de comprometimento.
            </p>
            <p class="Texto">
            <strong>b)</strong> Monitoramento sistêmico: Na nova versão é pontuado sobre a necessidade de assegurar que sistemas para gerenciar riscos estejam implementados e operem eficazmente, lembrando que sistemas podem ser informatizados ou não.
            </p>
            <p class="Texto">
            <strong>c)</strong> Contexto da organização: Ao se analisar o contexto da organização, foi incluído um ponto sobre considerar a complexidade das redes de relacionamento e dependências, de partes interessadas.
            </p>
            <p class="Texto">
            <strong>d)</strong> Comunicação e consulta: É ressaltada a importância da comunicação e consulta com as partes interessadas de forma mais genérica, porém destacando a relevância de desenvolver melhorias a partir das informações coletadas com a consulta.
            </p>
            <p class="Texto">
            <strong>e)</strong> Implementação: A nova redação tem mais foco em planejamento da estrutura, retirando citações objetivas referentes a manutenção de registros e atendimento a requisitos legais, que estavam presentes na versão anterior.
            </p>

            <p class="Texto">
            Na versão 2018 foi dada ênfase do caráter dinâmico e iterativo da gestão de riscos; assim como as orientações para identificação, análise e avaliação de riscos ficaram mais claras e didáticas; também a abordagem sobre documentação do processo de gestão de riscos e seus resultados ficou mais flexível; não há mais a presença do anexo que explicava os atributos de uma gestão de riscos avançada; empregou um discurso mais gerencial, ou estratégico; e foi reforçada a ideia de que a gestão de riscos é algo que deva ser intrínseca à organização.
            </p>
            <p class="Texto">
            Já o modelo COSO II aborda o paradigma de gestão de riscos por meio de um modelo integrado composto pelas dimensões: ambiente interno; definição de objetivos; identificação de eventos; avaliação de riscos; resposta ao risco; atividades de controle; informação e comunicação; e monitoramento (COSO, 2007). As especificações, alcances e significados desse modelo integrado serão apresentados na próxima seção desta aula. 
            </p>
            <p class="Texto">
            Por outro lado, Hill (2006) lembra que foram desenvolvidas várias outras estruturas de gestão de riscos com a finalidade de auxiliar os gestores a tratar os riscos de forma sistemática, citando, a título de exemplo: a gestão de riscos da Associação Canadense de Padrões (CAN/CSAQ850-97); o modelo de Critérios de Controle desenvolvido pelo <span style="color:#FC6B30;"> Instituto Canadense de Auditores Juramentados (</span>Canadian Institute of Chartered Accountants<span style="color: #FC6B30;">)</span>; a Estrutura de Gestão de Riscos em Políticas Públicas, do Grupo de Trabalho de Secretários Executivos Adjuntos para a Gestão de Riscos; e a estrutura para a Gestão Integrada de Riscos, do Conselho do Tesouro canadense. 
            </p>
            <p class="Texto">
            Hill (2006) destaca ainda que essas estruturas de gestão de risos desenvolvidas estão suportadas por uma base comum referente às principais etapas de um ciclo de processo decisório em gestão de riscos: identificação; avaliação; resposta; comunicação; e monitoramento e aprendizagem.
            </p>

